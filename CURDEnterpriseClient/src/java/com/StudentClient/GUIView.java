    private void createStudentTable(){
        DefaultTableModel model =  (DefaultTableModel) jTable3.getModel();
        System.out.println("Size student " + studentClient.getStudentData().size());
        for(int i=0; i< studentClient.getStudentData().size(); i++){
            Object[] row = {
                studentClient.getStudentData().get(i).get(0),
                studentClient.getStudentData().get(i).get(1),
                studentClient.getStudentData().get(i).get(5),  // 5
                studentClient.getStudentData().get(i).get(2),  // 2
                studentClient.getStudentData().get(i).get(4),
                studentClient.getStudentData().get(i).get(7),  // 7
                studentClient.getStudentData().get(i).get(3),  // 3
                studentClient.getStudentData().get(i).get(6)
            };
            model.addRow(row);
        }
    }
    
    private void refreshTabelDisplay(){
        DefaultTableModel model =  (DefaultTableModel) jTable3.getModel();
        for(int i = model.getRowCount() - 1; i>= 0;){
            System.out.println("remove " + i);
            model.removeRow(i);
            i = model.getRowCount() - 1;
        }
        this.createStudentTable();
    }
    
    private void addTableListener(){
        jTable3.getModel().addTableModelListener(new TableModelListener() {
            @Override
            public void tableChanged(TableModelEvent e) {
                if(e.getColumn() == -1){
                    return;
                }
                System.out.println(e.getLastRow() + "    " + e.getColumn());
                studentClient.updateStudent(Integer.parseInt((String) jTable3.getModel().getValueAt(e.getLastRow(), 0)),
                        e.getColumn(),
                        (String) jTable3.getModel().getValueAt(e.getLastRow(), e.getColumn()));
            }
            
        });
        
        
    }
    
   
    
    
