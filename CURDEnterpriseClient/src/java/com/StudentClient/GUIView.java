    private void addRightClickEvent() {
        jTable3.addMouseListener(new MouseAdapter(){
            @Override
            public void mousePressed(MouseEvent e){
            if (e.isPopupTrigger())
                doPop(e);
            }

            @Override
            public void mouseReleased(MouseEvent e){
                if (e.isPopupTrigger())
                    doPop(e);
            }

            private void doPop(MouseEvent e){
                int row = jTable3.rowAtPoint(e.getPoint());
                
                int id = Integer.parseInt((String) jTable3.getModel().getValueAt(row, 0));
                JPopupMenu jpm = new JPopupMenu();
                JMenuItem anItem = new JMenuItem("Delete this student record");
                anItem.addMouseListener(new MouseListener() {
                @Override
                public void mouseClicked(MouseEvent e) {
                }

                @Override
                public void mousePressed(MouseEvent e) {
                    
                    System.out.println(id);
                    studentClient.deleteStudentRecord(id);
                    ((DefaultTableModel) jTable3.getModel()).removeRow(row);
                }

                @Override
                public void mouseReleased(MouseEvent e) {
                    }

                @Override
                public void mouseEntered(MouseEvent e) {
                   }

                @Override
                public void mouseExited(MouseEvent e) {
                 }
                
               
            });
                jpm.add(anItem);
                jpm.show(e.getComponent(), e.getX(), e.getY());
            }
        });
        //jTable3.rowAtPoint(evt.getPoint());
    }


